package Loops;
import java.util.*;
public class Hello_Print_100times {
    public static void main(String[]args){
//        Scanner sc = new Scanner(System.in);
        int counter = 0;
        while(counter<100){
            System.out.println("Hello world! ");
            counter ++;
        }
        System.out.println("Hello printed finish 100x");
    }
}
